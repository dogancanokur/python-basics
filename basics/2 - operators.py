# aritmetic operators
print(1 + 2)  # 3
print(10 - 3)  # 7
print(5 / 2)  # 2.5
print(8 // 3)  # 2
print(10 % 4)  # 2

a = 5
b = 10
c = (a + b) * a / b
print(c)  # 7.5

# string concat
a = "first"
b = "second"
print(a + b)  # firstsecond
print(a * 2)  # firstfirst
print("* " * 1)  # *
print("* " * 2)  # **
print("* " * 3)  # ***
print("* " * 4)  # ****

# get element by index
a = "Python"
print(a[2])  # t
b = [1, 2, 3, 4, 5]
print(b[2])  # 3
# array/string length
print(len(a))  # 6

# last element
print(a[len(a) - 1])  # n
print(b[len(b) - 1])  # 5

# get elements by indexes
print(a[1:4])  # yth
print(a[2:])  # thon
print(a[:3])  # Pyt
print(b[1:4])  # [2, 3, 4]
print(b[2:])  # [3, 4, 5]
print(b[:3])  # [1, 2, 3]

print(b[0:6:2])  # [1, 3, 5] # 0dan 6ya kadar olanlari al ama 2ser atlayarak git
print(a[::2])  # Pto


